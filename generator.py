texts = set()
attempts = 0

valid_results = {}
topic_index = 0
failed_attempts = 0

while len(valid_results) < 50 and attempts < 200000:
    attempts += 1
    
    # Strictly alternate topics instead of random
    t_topic = topics[topic_index % len(topics)]
    
    s1 = random.choice(intros).format(topic=t_topic)
    s2 = random.choice(zdo)
    s3 = random.choice(samo)
    s4 = random.choice(kak_vy)
    s5 = random.choice(sosto)
    
    body = f"{s1} {s2} {s3} {s4} {s5}"
    words = body.split()
    L = len(words)
    needed = 100 - L
    
    if needed in paddings:
        final_text = body + " " + random.choice(paddings[needed])
        actual_words = final_text.split()
        if len(actual_words) == 100:
            if final_text not in texts:
                texts.add(final_text)
                valid_results[str(len(valid_results) + 1)] = final_text
                topic_index += 1  # Move to the next topic only when we get a valid distinct text
                failed_attempts = 0
            else:
                failed_attempts += 1
        else:
            failed_attempts += 1
    else:
        failed_attempts += 1
        
    # If a certain combination gets stuck with no possible variation for a topic, force move:
    if failed_attempts > 1000:
        topic_index += 1
        failed_attempts = 0

print(f"Generated {len(valid_results)}. Creating JSON...")
if len(valid_results) == 50:
    for k, t in valid_results.items():
        assert len(t.split()) == 100, f"Error at {k}"
        for kw in ["ЗДОРОВЬЕ", "САМОЧУВСТВИЕ", "КАК ВЫ", "СОСТОЯНИЕ"]:
            assert kw in t, f"Missing {kw} in {k}"
            
    with open("safety_texts.json", "w", encoding="utf-8") as f:
        json.dump(valid_results, f, ensure_ascii=False, indent=4)
    print("Success! Saved to safety_texts.json")
else:
    print("Failed to generate 50 distinct texts.")
